DROP TABLE IF EXISTS CUSTOMER CASCADE;
CREATE TABLE CUSTOMER (
	ID SERIAL PRIMARY KEY,
	NAME TEXT NOT NULL UNIQUE,
	EMAIL TEXT,
	PASSWORD TEXT NOT NULL
);

